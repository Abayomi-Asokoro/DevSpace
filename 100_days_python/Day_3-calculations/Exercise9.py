# Calculate the midpoint of the segment with ends at the points: A = (2, 4), B = (-4, 6) and print result to the console as shown below.


# Expected result: The middle point: (-1.0, 5.0)


x1,x2= 2, 4
y1,y2= -4, 6

Mxyi = (x1 + y1)/2
Mxyii = (x2 + y2)/2

MPxy = (Mxyi, Mxyii)

print(f"The middle point: {MPxy}")


